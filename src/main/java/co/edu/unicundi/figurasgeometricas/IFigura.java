package co.edu.unicundi.figurasgeometricas;

/**
 * Interface IFigura
 * permite realizar un conjunto de declaraciones de métodos sin implementación
 * Todas las clases que implementen la interface IFigura se ve obligada a implementar el metodo getNombre()
 * @author LAURA BABATIVA
 */
public interface IFigura {
    public abstract String getNombre();
}
